export const getServerSideProps: GetServerSideProps = async (ctx) => {
  const { 'drawingauth.token': token } = parseCookies(ctx)

  if (!token) {
    return {
      redirect: {
        destination: "/login",
        permanent: false
      }
    }
  }

  return {
    props: {}
  }
}

